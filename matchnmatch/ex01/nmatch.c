int		nmatch(char *s1, char *s2)
{
	int i;

	i = 0;
	if (*s2 == '*' && *s1 != '\0')
	{
		i += nmatch(s1 + 1, s2);
		i += nmatch(s1, s2 + 1);
	}
	else if (*s2 == '*' && *s1 == '\0')
		i += nmatch(s1, s2 + 1);
	else if (*s1 == *s2 && *s1 != '\0')
		i += nmatch(s1 + 1, s2 + 1);
	else
		i += (*s1 == '\0' && *s2 == '\0');
	return (i);
}
