int		ft_strcmp(char *s1, char *s2)
{
	char	c;
	char	d;
	int		i;

	i = 0;
	while (s1[i] == s2[i])
	{
		if (s1[i] == '\0')
			return (0);
		i++;
	}
	c = s1[i];
	d = s2[i];
	return ((unsigned char)c - (unsigned char)d);
}
